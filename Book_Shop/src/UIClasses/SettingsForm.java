private void generateDatabaseCust() {

        try {
            Class.forName("com.mysql.jdbc.Driver");
            BufferedReader reader = new BufferedReader(new FileReader("C:/MEDISAFE/config/msdata.of"));
            String line = null;
            while (reader.ready()) {
                line = reader.readLine();
            }
            String ar[] = line.split("-");

            Connection connection = DriverManager.getConnection("jdbc:mysql://" + ar[2] + ":" + ar[3], ar[0], ar[1]);

            BufferedReader readerBACK = new BufferedReader(new FileReader("C:/NISAKMA/config/dbcr.of"));
            String lineBACK = null;
            while (readerBACK.ready()) {
                lineBACK = readerBACK.readLine();
                connection.createStatement().executeUpdate(lineBACK);
            }
            new Messages(1, "Created", "The database successfully created") {

                @Override
                void AfterMessageClosed() {
                }
            }.setVisible(true);
        } catch (Exception e) {
            new Messages(3, "Error", "Cannot create databse") {

                @Override
                void AfterMessageClosed() {
                }
            }.setVisible(true);
        }

    }

