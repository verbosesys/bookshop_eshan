    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            BufferedReader reader = new BufferedReader(new FileReader("C:/BSHOP/config/msdata.of"));
            String line = null;
            while (reader.ready()) {
                line = reader.readLine();
            }
            String ar[] = line.split("-");

            if ((!tfmscurrentuser.getText().equals(ar[0])) | (!new String(jpmscurrentpass.getPassword()).equals(ar[1]))) {
                es.toolt(tfmscurrentuser, "Username or password incorrect");
                jpmscurrentpass.setText("");
                return;
            }
            if ((tfmsnewuser.getText().equals("")) | (new String(jpmsnewpass.getPassword()).equals(""))) {
                es.toolt(tfmsnewuser, "Enter a new username & password");
                return;
            }

            FileOutputStream fos = new FileOutputStream("C:/BSHOP/config/msdata.of");
            String text = tfmsnewuser.getText() + "-" + new String(jpmsnewpass.getPassword()) + "-" + ar[2] + "-" + ar[3];
            fos.write(text.getBytes());
            fos.flush();
            fos.close();

            tfmscurrentuser.setText("");
            jpmscurrentpass.setText("");
            tfmsnewuser.setText("");
            jpmsnewpass.setText("");

            new Messages(1, "Change success", "MySql username & password changed") {
                @Override
                void AfterMessageClosed() {
                }
            }.setVisible(true);

        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

