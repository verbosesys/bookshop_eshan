    private void INVno(boolean isupdate) throws Exception {
        ResultSet nosrs = DB.search("SELECT no FROM nos WHERE cat=1");
        int no = 0;
        while (nosrs.next()) {
            no = nosrs.getInt(1);
        }
        if (isupdate) {
            no++;
            DB.Execute("UPDATE nos SET no='" + no + "' WHERE cat=1 ");
        }
        lbinvno.setText("" + no);
    }

    private void saveBILL() throws Exception {
        for (int i = 0; i < tbModleinv.getRowCount(); i++) {
            String pid = tbModleinv.getValueAt(i, 1).toString();
            String pidnow = pid;
            double nowQTY = Double.parseDouble(tbModleinv.getValueAt(i, 3).toString());

            if (nowQTY < 0) {
                pidnow = pid.substring(0, pid.length() - 2);
            }
            DB.Execute("INSERT INTO inv_data VALUES('" + lbinvno.getText() + "','" + tbModleinv.getValueAt(i, 0) + "','" + tbModleinv.getValueAt(i, 1) + "','" + tbModleinv.getValueAt(i, 2) + "','" + nowQTY + "','" + tbModleinv.getValueAt(i, 4) + "','" + tbModleinv.getValueAt(i, 5) + "','" + tbModleinv.getValueAt(i, 6) + "')");
            //UPDATE STOCK START..............

            ResultSet rsSTOCK = DB.search("SELECT stock FROM items WHERE iditems='" + pid + "'");
            double curStock = 0;
            double nowSTOCK = 0;

            if (rsSTOCK.next()) {
                curStock = rsSTOCK.getDouble(1);
            }

            nowSTOCK = curStock - (nowQTY);
            DB.Execute("UPDATE items SET stock='" + nowSTOCK + "'  WHERE iditems='" + pidnow + "' ");
            //UPDATE STOCK END..............
        }
        String dtTM = lbdateinv.getText();
        String timeTM = lbtimeinv.getText();
        String tot = tftot.getText().replace(",", "");
        String subtot = tfsubtot.getText().replace(",", "");
        String rec = tfcshinv.getText().replace(",", "");
        String bal = tfbalinv.getText().replace(",", "");
        String dis = tfdis.getText().replace(",", "");
        String disamo = tfqty3.getText().replace(",", "");
        String items = tfqty2.getText();

        DB.Execute("INSERT INTO inv_header VALUES('" + lbinvno.getText() + "','" + dtTM + "','" + timeTM + "','" + lbcashier.getText() + "','" + tfcustName.getText() + "','" + subtot + "','" + tot + "','" + dis + "','" + disamo + "','" + items + "','" + rec + "','" + bal + "')");
        DB.Execute("INSERT INTO payments VALUES('" + lbinvno.getText() + "','" + dtTM + "','" + timeTM + "','" + tot + "','" + lbcashier.getText() + "')");
        INVno(true);

    }

