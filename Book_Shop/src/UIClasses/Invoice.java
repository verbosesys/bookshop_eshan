    private void calprices(int i) {
        try {
            if (i == 1) {
                for (int j = 0; j < jTable1.getRowCount(); j++) {
                    String itemID = tbModleinv.getValueAt(j, 1).toString();
                    ResultSet rs = DB.search("SELECT discount,sell_price FROM items WHERE iditems='" + itemID + "'");
                    if (rs.next()) {
                        String disc = rs.getString(1);
                        String sellPrice = rs.getString(2);
                        tbModleinv.setValueAt(disc, j, 5);
                        tbModleinv.setValueAt(sellPrice, j, 4);
                        double up = rs.getDouble(2);
                        String newp;

                        if (disc.isEmpty()) {
                            disc = "0";
                        }

                        String lastChar = disc.substring(disc.length() - 1);
                        if (lastChar.equals("%")) {
                            double tot = up * Double.parseDouble(tbModleinv.getValueAt(j, 3).toString());
                            String dis = disc.substring(0, disc.length() - 1);
                            newp = (dfNor.format(tot - tot * (Double.parseDouble(dis) / 100)));
                        } else {
                            double tot = up * Double.parseDouble(tbModleinv.getValueAt(j, 3).toString());
                            double dis = Double.parseDouble(disc);
                            newp = (dfNor.format(tot - dis));
                        }

                        tbModleinv.setValueAt(newp, j, 6);

                    }
                }
                calTotal();
            } else if (i == 2) {

                for (int j = 0; j < jTable1.getRowCount(); j++) {
                    String itemID = tbModleinv.getValueAt(j, 1).toString();
                    ResultSet rs = DB.search("SELECT sub_category,sell_price FROM items WHERE iditems='" + itemID + "'");
                    if (rs.next()) {
                        ResultSet rs2 = DB.search("SELECT catdis FROM subcategory WHERE catname='" + rs.getString(1) + "'");
                        if (rs2.next()) {
                            String disc = rs2.getString(1);
                            tbModleinv.setValueAt(disc, j, 5);
                            double up = rs.getDouble(2);
                            String newp;

                            if (disc.isEmpty()) {
                                disc = "0";
                            }
                            String lastChar = disc.substring(disc.length() - 1);
                            if (lastChar.equals("%")) {
                                double tot = up * Double.parseDouble(tbModleinv.getValueAt(j, 3).toString());
                                String dis = disc.substring(0, disc.length() - 1);
                                newp = (dfNor.format(tot - tot * (Double.parseDouble(dis) / 100)));
                            } else {
                                double tot = up * Double.parseDouble(tbModleinv.getValueAt(j, 3).toString());
                                double dis = Double.parseDouble(disc);
                                newp = (dfNor.format(tot - dis));
                            }

                            tbModleinv.setValueAt(newp, j, 6);
                            tbModleinv.setValueAt(up, j, 4);

                        }

                    }
                }
                calTotal();
            } else if (i == 3) {

                for (int j = 0; j < jTable1.getRowCount(); j++) {
                    String itemID = tbModleinv.getValueAt(j, 1).toString();
                    ResultSet rs = DB.search("SELECT sell_price2 FROM items WHERE iditems='" + itemID + "'");
                    if (rs.next()) {
                        tbModleinv.setValueAt(rs.getString(1), j, 4);
                        tbModleinv.setValueAt("", j, 5);
                        double subtot = Double.parseDouble(tbModleinv.getValueAt(j, 3).toString()) * Double.parseDouble(tbModleinv.getValueAt(j, 4).toString());
                        tbModleinv.setValueAt(String.valueOf(subtot), j, 6);
                    }
                }
                calTotal();
            }
        } catch (Exception e) {
            e.printStackTrace();
        }

    }
