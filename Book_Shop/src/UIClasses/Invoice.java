    private void calTotal() {
        double total = 0.00;
        double distot = 0.00;
        for (int i = 0; i < tbModleinv.getRowCount(); i++) {
            tbModleinv.setValueAt(i + 1, i, 0);
            total = total + Double.parseDouble(tbModleinv.getValueAt(i, 6).toString());

            distot = distot + (Double.parseDouble(tbModleinv.getValueAt(i, 4).toString()) * Double.parseDouble(tbModleinv.getValueAt(i, 3).toString())) - Double.parseDouble(tbModleinv.getValueAt(i, 6).toString());
        }
        tfsubtot.setText(dftot.format(total));
        tftot.setText(dftot.format(total));
        tfdis.setText("");
        tfqty2.setText(tbModleinv.getRowCount() + "");
        tfqty3.setText(dfNor.format(distot));

        tfcshinv.setText("");
        tfbalinv.setText("");

    }

