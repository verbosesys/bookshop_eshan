    private void setColorsTB(JTable table1) {
        final ArrayList ar = new ArrayList();

        for (int i = 0; i < table1.getRowCount(); i++) {
            ar.add(i);
        }

        table1.setDefaultRenderer(Object.class, new DefaultTableCellRenderer() {
            @Override
            public Component getTableCellRendererComponent(JTable table, Object value, boolean isSelected, boolean hasFocus, int row, int column) {
                final Component c = super.getTableCellRendererComponent(table, value, isSelected, hasFocus, row, column);
                if (Integer.parseInt(ar.get(row).toString()) % 2 == 1) {
                    c.setBackground(new java.awt.Color(253, 253, 253));
                } else {
                    c.setBackground(new java.awt.Color(240, 240, 240));
                }
                return c;
            }
        });
    }

    private void printBill() throws PrinterException {
        try {
            Map<String, Object> params = new HashMap<String, Object>();
            params.put("para_invno", lbinvno.getText());
            params.put("para_datetm", lbdateinv.getText() + " " + lbtimeinv.getText());
            params.put("para_cashr", lbcashier.getText());
            params.put("para_tot", tftot.getText());

            if (tfcshinv.getText().isEmpty()) {
                params.put("para_cash", tftot.getText());
                params.put("para_bal", "0.00");
            } else {
                params.put("para_cash", tfcshinv.getText());
                params.put("para_bal", tfbalinv.getText());
            }

            params.put("para_items", tfqty2.getText());

            params.put("para_tota", tfsubtot.getText());
            String cust = tfcustName.getText();
            if (cust.equals("")) {
                cust = "GUEST CUSTOMER";
            }
            params.put("para_cust", cust);
            params.put("para_disc", tfqty3.getText());

            JasperReport jasperReport = JasperCompileManager.compileReport("C:/BSHOP/BILL.jrxml");
            JasperPrint jasperPrint = JasperFillManager.fillReport(jasperReport, params, new JRTableModelDataSource(jTable1.getModel()));
            //JasperViewer.viewReport(jasperPrint, false);
            JasperPrintManager.printReport(jasperPrint, false);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

