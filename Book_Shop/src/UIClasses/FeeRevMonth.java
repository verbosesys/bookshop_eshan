    private void setMonths(int year, int month, int day) {
        Calendar cal = new GregorianCalendar();
        Calendar cal2 = new GregorianCalendar();
        cal2.setFirstDayOfWeek(Calendar.MONDAY);
        cal.setFirstDayOfWeek(Calendar.MONDAY);

        if (year != 0) {
            cal.set(Calendar.YEAR, year);
        }
        if (month != 0) {
            cal.set(Calendar.MONTH, month - 1);
        }
        if (day != 0) {
            cal.set(Calendar.DATE, day);
        }
        int CMONTH = cal.get(GregorianCalendar.MONTH);
        int get1 = cal.getActualMaximum(GregorianCalendar.DAY_OF_MONTH);
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        for (int i = 0; i < dtm.getRowCount(); i++) {
            for (int j = 0; j < dtm.getColumnCount(); j++) {
                dtm.setValueAt("", i, j);
            }
        }
        TableCellRenderer cellPrevious = null;
        for (int i = 1; i <= get1; i++) {
            cal2.set(cal.get(Calendar.YEAR), CMONTH, i);
            String Day = new SimpleDateFormat("u").format(cal2.getTime());
            int Dy = Integer.parseInt(Day) - 1;
            int initDay = cal2.get(Calendar.WEEK_OF_MONTH) - 1;
            dtm.setValueAt("" + i, initDay, Dy);
            if (JlMonth.getText().equals(nowMonth) & Jlyear.getText().equals(nowYear)) {
                cellPrevious = jTable1.getColumnModel().getColumn(0).getCellRenderer();
                jTable1.getColumnModel().getColumn(Dy).setCellRenderer(new CustomRenderer(nowDate));
            } else {
                jTable1.getColumnModel().getColumn(Dy).setCellRenderer(cellPrevious);
            }
        }
        final DefaultTableCellRenderer rightRenderer = (DefaultTableCellRenderer) jTable1.getCellRenderer(0, 0);
        rightRenderer.setHorizontalAlignment(SwingConstants.CENTER);
        for (int i = 0; i < 7; i++) {
            jTable1.getColumnModel().getColumn(i).setCellRenderer(rightRenderer);
        }
    }

    private String SetDates(boolean UPNO) {
        String Todate = null;
        Calendar calendar = new GregorianCalendar();
        if (UPNO) {
            CURYear = calendar.get(Calendar.YEAR);
            CURMonth = calendar.get(Calendar.MONTH) + 1;
            CURDate = calendar.get(Calendar.DATE);
        }
        Todate = CURYear + "-" + getMonthInName(CURMonth) + "-" + CURDate;
        Jlyear.setText("" + CURYear);
        JlMonth.setText(getMonthInName(CURMonth));
        return Todate;
    }

    private void updateCurrentDate(int status) {
        CURYear = Integer.parseInt(Jlyear.getText());
        CURMonth = Integer.parseInt(getMonthInNumber(JlMonth.getText()));

        if (status == 1) {
            int yy = CURYear - 1;
            CURYear = yy;
            Jlyear.setText("" + CURYear);
        } else if (status == 2) {
            int yy = CURYear + 1;
            CURYear = yy;
            Jlyear.setText("" + CURYear);
        } else if (status == 3) {
            if (CURMonth == 1) {
                CURMonth = 13;
            }
            int yy = CURMonth - 1;
            CURMonth = yy;
            JlMonth.setText(getMonthInName(CURMonth));
        } else if (status == 4) {
            if (CURMonth == 12) {
                CURMonth = 0;
            }
            int yy = CURMonth + 1;
            CURMonth = yy;
            JlMonth.setText(getMonthInName(CURMonth));
        }

        setMonths(CURYear, CURMonth, CURDate);
    }

