    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            if ("".equals(tfUsername.getText())) {
                es.toolt(tfUsername, "Enter username");
                return;
            }
            if ("".equals(new String(passwordFIELD.getPassword()))) {
                es.toolt(passwordFIELD, "Enter password");
                return;
            }
            tfUsername.setCursor(new Cursor(Cursor.WAIT_CURSOR));
            ResultSet rs = DB.search("SELECT * FROM user WHERE username='" + tfUsername.getText() + "' AND password='" + new String(passwordFIELD.getPassword()) + "'");
            if (rs.next()) {
                CheckUser checkUser = new CheckUser();
                checkUser.setLoggedUsername(rs.getString(1));
                checkUser.setLoggedPerson(rs.getString(3));

               // es.animOut(this);
                new Invoice().setVisible(true);
                this.dispose();
            } else {
                es.toolt(passwordFIELD, "Username or password is incorrect");
                passwordFIELD.setText("");
            }
            tfUsername.setCursor(new Cursor(Cursor.DEFAULT_CURSOR));
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

