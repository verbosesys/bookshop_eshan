    private void chklamStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_chklamStateChanged
        if (chklam.isSelected() == true) {
            double oldamt = 0d;
                if (!tfsubtot.getText().isEmpty()) {
                    oldamt = Double.parseDouble(tfsubtot.getText());
                }
            try {
                String query = "SELECT price FROM photocopy WHERE type1 = 'LAMINATE' AND size = '" + tfpagesize.getText() + "'";
                System.out.println(query);
                ResultSet rs = DB.search(query);
                if (rs.next()) {
                    double newamt = oldamt + (rs.getDouble(1) * Double.parseDouble(tfqty.getText()));
                    tfsubtot.setText(String.valueOf(newamt));
                    tempPriceLM = rs.getDouble(1);
                }
            } catch (Exception e) {
                e.printStackTrace();
            }         
        }
        if (chklam.isSelected() == false) {
            double oldamt = 0d;
                if (!tfsubtot.getText().isEmpty()) {
                    oldamt = Double.parseDouble(tfsubtot.getText());
                }
            try {
                String query = "SELECT price FROM photocopy WHERE type1 = 'LAMINATE' AND size = '" + tfpagesize.getText() + "'";
                System.out.println(query);
                ResultSet rs = DB.search(query);
                if (rs.next()) {
                    double newamt = oldamt - (rs.getDouble(1) * Double.parseDouble(tfqty.getText()));
                    tfsubtot.setText(String.valueOf(newamt));
                    tempPriceLM = rs.getDouble(1);
                }
            } catch (Exception e) {
                e.printStackTrace();
            }         
        }
    }//GEN-LAST:event_chklamStateChanged

