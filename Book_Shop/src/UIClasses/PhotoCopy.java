    private void queryGenerator(int option) {
        String size = tfpagesize.getText();
        String type1 = null;
        if (option == 1) {
            type1 = "PHOTOCOPY";
        } else if (option == 2) {
            type1 = "PRINTOUT";
        } else if (option == 3) {
            type1 = "LAMINATE";
        }
        String type2 = tftype.getText();
        String side = "SINGLE";
        if (jrbdouble.isSelected()) {
            side = "DOUBLE";
        }
        String query = "SELECT id FROM photocopy WHERE type1 = '" + type1 + "' AND type2 = '" + type2 + "' AND size = '" + size + "' AND side = '" + side + "'";
        try {
            ResultSet rs = DB.search(query);
            System.out.println(query);
            if (rs.next()) {
                String subquery = "SELECT price FROM photocopy_price WHERE id = '" + rs.getString(1) + "' AND minq < '" + tfqty.getText() + "'";
                ResultSet rs2 = DB.search(subquery);
                System.out.println(subquery);
                if (rs2.next()) {
                    double amount = Double.parseDouble(tfqty.getText()) * rs2.getDouble(1);
                    tfsubtot.setText(String.valueOf(amount));
                    tempPricePC = rs2.getDouble(1);
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
