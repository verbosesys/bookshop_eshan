    public void searchALL(String query) throws Exception {
        ResultSet rs = DB.search(query);
        TbModleCAT.setRowCount(0);
        while (rs.next()) {
            Vector v = new Vector();
            v.add(rs.getString(1));
            v.add(rs.getString(2));
            v.add(rs.getString(3));
            v.add(rs.getString(4));
            TbModleCAT.addRow(v);
        }
    }

    public void clearALL() throws Exception {
        JTextField tf[] = {tfcatNo, tfcatName, tfdiscription, tfdiscount};
        es.clearTFS(tf);
        searchALL("SELECT * FROM subcategory");
        tfcatNo.grabFocus();
        INVno(false);
    }

    private void INVno(boolean isupdate) throws Exception {
        ResultSet nosrs = DB.search("SELECT no FROM nos WHERE cat=5");
        int no = 0;
        while (nosrs.next()) {
            no = nosrs.getInt(1);
        }
        if (isupdate) {
            no++;
            DB.Execute("UPDATE nos SET no='" + no + "' WHERE cat=5 ");
        }
        tfcatNo.setText("" + no);
    }
