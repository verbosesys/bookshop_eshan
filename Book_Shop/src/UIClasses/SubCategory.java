    private void jbcat_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbcat_saveActionPerformed
        try {
            JTextField tf[] = {tfcatNo, tfcatName};
            if (!es.checkEMPTY(tf)) {
                ResultSet rs = DB.search("SELECT * FROM subcategory WHERE catid='" + tfcatNo.getText() + "'");
                if (rs.next()) {
                    new ConfirmMessage("Modification", "Are you sure you want to modify data?", false) {

                        @Override
                        void ifYes() {
                            try {
                                DB.Execute("UPDATE subcategory SET catname='" + tfcatName.getText() + "',catdescription='" + tfdiscription.getText() + "', catdis='"+tfdiscount.getText()+"' WHERE catid='" + tfcatNo.getText() + "'");
                                clearALL();
                            } catch (Exception e) {
                                e.printStackTrace();
                            }
                        }
                        @Override
                        void ifNo() {
                            return;
                        }
                    }.setVisible(true);
                } else {
                    DB.Execute("INSERT INTO subcategory VALUES('" + tfcatNo.getText() + "','" + tfcatName.getText() + "','" + tfdiscription.getText() + "', '"+tfdiscount.getText()+"')");
                    INVno(true);
                    jTable1.changeSelection(jTable1.getRowCount() - 1, 0, false, false);
                    clearALL();
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
            //new SysErrMsg(e).setVisible(true);
        }
    }//GEN-LAST:event_jbcat_saveActionPerformed

