    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        if (tfdis.getText().isEmpty()) {
            jButton6ActionPerformed(evt);
            addDiscount();
        } else {
            String lastChar = tfdis.getText().substring(tfdis.getText().length() - 1);
            if (lastChar.equals("%")) {
                double tot = Double.parseDouble(tfsubtot.getText().replace(",", ""));
                String dis = tfdis.getText().substring(0, tfdis.getText().length() - 1);
                tftot.setText(dftot.format(tot - tot * (Double.parseDouble(dis) / 100)));
            } else {
                double tot = Double.parseDouble(tfsubtot.getText().replace(",", ""));
                double dis = Double.parseDouble(tfdis.getText());
                tftot.setText(dftot.format(tot - dis));
            }
        }
        tfsearchItems.grabFocus();
        tfsearchItems.selectAll();
        addDiscount();
    }//GEN-LAST:event_jButton7ActionPerformed

