    private void jTable2KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTable2KeyReleased
        if (evt.getKeyCode() == 10) {
            tfid.setText(tbModleitem.getValueAt(jTable2.getSelectedRow(), 1).toString());
            jPopupMenu1.setVisible(false);

            try {
                ResultSet rs = DB.search("SELECT sell_price,sell_price2,discount,itemname FROM items WHERE iditems='" + tfid.getText() + "'");
                if (rs.next()) {
                    tfsearchItems.setText(rs.getString(4));
                    tfprice.setText(rs.getString(1));
                    tfdiscount.setText(rs.getString(3));
                    tfqty.setText("1");
                    tfqty.grabFocus();
                    tfqty.selectAll();

                }
            } catch (Exception e) {
                e.printStackTrace();
            }

        } else if (evt.getKeyCode() != 38 && evt.getKeyCode() != 40 && KeyEvent.getKeyText(evt.getKeyCode()).length() == 1) {
            tfsearchItems.grabFocus();
            tfsearchItems.selectAll();
            tfsearchItems.setText(tfsearchItems.getText() + evt.getKeyChar());
        }
    }//GEN-LAST:event_jTable2KeyReleased

